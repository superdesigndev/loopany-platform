/**
 * Best-effort local install of the loopany agent skill via the `npx skills` CLI
 * (vercel-labs/skills). Installed at USER scope (`~/.claude/skills/loopany/`) to
 * match the daemon's per-machine scope: Claude Code discovers user-level skills
 * globally, so a loop agent in ANY workdir still triggers the create/update/evolve
 * references — no more per-workdir copies scattering. It's fired at `loopany up`
 * (refreshing the install to whatever daemon version just launched) and again after
 * a successful `loopany new`, and it NEVER blocks: any failure (no network, no npx,
 * no write permission, bundled skill absent) degrades silently to the always-working
 * /api/skill inline path. It just prints one status line. `loopany skill install`
 * is the manual escape hatch (`--project` installs into the cwd instead).
 *
 * (History: 0.4.0 deliberately kept skill logic OUT of `up` because project scope
 * would pollute an arbitrary cwd; the flip to user scope removes that hazard, so
 * `up` is now the natural refresh point — version-locked to the daemon it launches.)
 *
 * We install from the skill BUNDLED INTO THIS PACKAGE (synced from
 * packages/server/src/skill/ by scripts/sync-skill.mjs at build/prepublish) — a
 * LOCAL path source, so end users never need the private platform repo and the
 * install works offline once `skills` itself is cached. The exact invocation
 *   npx --yes skills add <dir> -a claude-code -y --copy -g
 * was verified against the current `skills` CLI (`-g` targets the user dir →
 * ~/.claude/skills/loopany/; `-y` is non-interactive + idempotent-overwrite;
 * `--copy` makes a self-contained copy, no symlink into this package's temp dir).
 * Dropping `-g` (the `--project` escape hatch) installs into the runner's cwd →
 * <cwd>/.claude/skills/loopany/.
 */
import { spawn } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const moduleDir = path.dirname(fileURLToPath(import.meta.url));

/** Hard ceiling on the `npx skills` call so a hung install can't wedge `loopany new`. */
const INSTALL_TIMEOUT_MS = 90_000;

/**
 * The skill dir bundled into this package. `../skill` resolves the same from both
 * `src/` (tsx dev) and `dist/` (built) since each sits beside the package-root
 * `skill/`. Generated by scripts/sync-skill.mjs; absent in a source-only checkout
 * that was never built (then install best-effort-skips).
 */
export function bundledSkillDir(base = moduleDir): string {
  return path.join(base, "..", "skill");
}

/** Whether the bundled skill is actually present (its SKILL.md exists). */
export function bundledSkillAvailable(dir = bundledSkillDir()): boolean {
  try {
    return fs.statSync(path.join(dir, "SKILL.md")).isFile();
  } catch {
    return false;
  }
}

export interface InstallOutcome {
  ok: boolean;
  /** One human-facing line to print (announced, best-effort). */
  line: string;
}

/** Result of running the installer command. Injected in tests so no network/npx. */
export interface RunResult {
  code: number;
  stdout: string;
  stderr: string;
}
export type Runner = (cmd: string, args: string[], opts?: { cwd?: string }) => Promise<RunResult>;

/** Spawn `npx skills …`, capture output, bounded by INSTALL_TIMEOUT_MS. Resolves
 *  (never rejects) with code -1 when the binary can't be spawned or times out. The
 *  child's `cwd` decides where a project-level install lands (`<cwd>/.claude/…`). */
const defaultRunner: Runner = (cmd, args, opts) =>
  new Promise<RunResult>((resolve) => {
    let stdout = "";
    let stderr = "";
    let child;
    try {
      child = spawn(cmd, args, { stdio: ["ignore", "pipe", "pipe"], cwd: opts?.cwd });
    } catch (err) {
      resolve({ code: -1, stdout: "", stderr: err instanceof Error ? err.message : String(err) });
      return;
    }
    const timer = setTimeout(() => {
      try {
        child.kill("SIGKILL");
      } catch {
        /* already gone */
      }
    }, INSTALL_TIMEOUT_MS);
    timer.unref?.();
    child.stdout?.on("data", (d) => (stdout += String(d)));
    child.stderr?.on("data", (d) => (stderr += String(d)));
    child.on("error", (err) => {
      clearTimeout(timer);
      resolve({ code: -1, stdout, stderr: err instanceof Error ? err.message : String(err) });
    });
    child.on("close", (code) => {
      clearTimeout(timer);
      resolve({ code: code ?? -1, stdout, stderr });
    });
  });

export interface InstallOpts {
  /** Install to the user dir (`~/.claude/skills`) instead of the project default. */
  global?: boolean;
  /** Project-level install target: the dir to run `npx skills` in, so the skill
   *  lands in `<cwd>/.claude/skills/loopany`. Defaults to the process cwd. Ignored
   *  when `global` is set (that always targets `~/.claude/skills`). */
  cwd?: string;
  /** Override the bundled skill dir (tests). */
  dir?: string;
  /** Override the command runner (tests) so nothing spawns npx / hits the network. */
  runner?: Runner;
}

/** The argv (after `npx`) for the install — pure, so tests can assert it. */
export function installArgs(dir: string, global = false): string[] {
  const args = ["--yes", "skills", "add", dir, "-a", "claude-code", "-y", "--copy"];
  if (global) args.push("-g");
  return args;
}

/**
 * Install/refresh the loopany skill. NEVER throws — returns an outcome with a
 * one-line status to print. Idempotent: `skills add -y` overwrites an existing
 * install with the bundled (current) content every time.
 */
export async function installSkill(opts: InstallOpts = {}): Promise<InstallOutcome> {
  const dir = opts.dir ?? bundledSkillDir();
  if (!bundledSkillAvailable(dir)) {
    return {
      ok: false,
      line: "loopany skill: skipped (bundled skill not found — falling back to /api/skill)",
    };
  }
  const runner = opts.runner ?? defaultRunner;
  // A project install runs in `cwd` (so it lands there); a global one ignores it.
  const cwd = opts.global ? undefined : opts.cwd;
  let res: RunResult;
  try {
    res = await runner("npx", installArgs(dir, opts.global), { cwd });
  } catch (err) {
    return { ok: false, line: `loopany skill: skipped (${err instanceof Error ? err.message : String(err)})` };
  }
  if (res.code === 0) {
    const where = opts.global
      ? "~/.claude/skills/loopany"
      : opts.cwd
        ? path.join(opts.cwd, ".claude", "skills", "loopany")
        : "./.claude/skills/loopany";
    return { ok: true, line: `loopany skill: installed → ${where}` };
  }
  const why = firstLine(res.stderr) || firstLine(res.stdout) || `exit ${res.code}`;
  return { ok: false, line: `loopany skill: skipped (${why}) — falling back to /api/skill` };
}

function firstLine(s: string): string {
  return (s || "").trim().split("\n")[0]?.trim() ?? "";
}

/**
 * The exact path our OLD (pre-user-scope) project installer created inside a loop
 * folder. Now that the skill lives at USER scope, any leftover copy here would
 * SHADOW it (project scope wins in Claude Code discovery) and go stale — so the
 * daemon best-effort removes it (see removeStaleProjectSkill).
 */
export function projectSkillDir(loopDir: string): string {
  return path.join(loopDir, ".claude", "skills", "loopany");
}

/** Read the `name:` from a SKILL.md's leading YAML frontmatter (`--- … ---`), or
 *  undefined when there's no frontmatter / no name. */
function skillFrontmatterName(md: string): string | undefined {
  const block = /^---\r?\n([\s\S]*?)\r?\n---/.exec(md);
  if (!block) return undefined;
  return /^\s*name:\s*(.+?)\s*$/m.exec(block[1])?.[1];
}

/**
 * True iff `dir` looks like a loopany skill install WE created — a SKILL.md whose
 * frontmatter declares `name: loopany`. The guard keeps the stale-copy cleanup from
 * ever deleting a dir that isn't ours (even though only our installer ever produces
 * this exact `.claude/skills/loopany` path). Never throws.
 */
export function isOurSkillDir(dir: string): boolean {
  try {
    return skillFrontmatterName(fs.readFileSync(path.join(dir, "SKILL.md"), "utf8")) === "loopany";
  } catch {
    return false;
  }
}

/**
 * Best-effort removal of a stale per-workdir loopany skill copy that would now
 * SHADOW the user-level install. Quiet + never throws; only deletes when the dir is
 * confirmed OURS (isOurSkillDir). Returns true iff a copy was actually removed.
 * Callers must have already applied the LOOPANY_ROOTS jail to `loopDir`.
 */
export function removeStaleProjectSkill(loopDir: string): boolean {
  const dir = projectSkillDir(loopDir);
  if (!isOurSkillDir(dir)) return false;
  try {
    fs.rmSync(dir, { recursive: true, force: true });
    return true;
  } catch {
    return false;
  }
}
